        │ 字段：ComparerName（比较器类型，如 val_compare）              │
        │       Parameters（比较参数，JSON 数组）                       │
        │                                                              │
        │ 端口：输出端口 0 → PassOutputs（条件满足时）                 │
        │       输出端口 1 → FailOutputs（条件不满足时）               │
        │                                                              │
        │ 用途：条件分支（如"击杀数>=5"、"资源>=1000"）                │
        │                                                              │
        │ 典型连接：Trigger → Comparer → Pass → Command(成功)          │
        │                              → Fail → Mission-F              │
        │                                                              │
        │ 可编辑字段：ComparerName, Parameters                         │
        │                                                              │
        │ 示例：检查击杀数 >= 5 → ComparerName="val_compare"           │
        │                        Parameters=[">=", "5"]                │
