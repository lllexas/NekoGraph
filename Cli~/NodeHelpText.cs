        │ 行为：调用游戏系统 → 执行命令 → 向输出节点传播               │
        │                                                              │
        │ 字段：Command.CommandName（命令名，如 spawn, play_cutscene） │
        │       Command.Parameter（单个参数，字符串）                  │
        │       Command.Parameters（多个参数，JSON 数组）              │
        │                                                              │
        │ 用途：执行操作（如"生成单位"、"播放动画"、"发放奖励"）        │
        │                                                              │
        │ 典型连接：Comparer(Pass) → Command(发放奖励) → Mission-S     │
        │                                                              │
        │ 可编辑字段：Command.CommandName, Command.Parameter,          │
        │            Command.Parameters                                │
        │                                                              │
        │ 示例：生成 3 个士兵 → CommandName="spawn"                     │
        │                      Parameter="soldier:3"                   │
        └─────────────────────────────────────────────────────────────┘

