        │                                                              │
        │ 行为：收到信号 → IsActive=true，向输出节点传播               │
        │                                                              │
        │ 字段：MissionID（任务唯一标识）                               │
        │       Priority（优先级）                                     │
        │       IsActive（是否激活）                                   │
        │       IsCompleted（是否完成）                                │
        │       IsFailed（是否失败）                                   │
        │                                                              │
        │ 用途：标记一个任务开始，通常后接 Trigger 等待条件              │
        │                                                              │
        │ 典型连接：Mission-A → Trigger(等待事件) → Comparer(检查条件) │
