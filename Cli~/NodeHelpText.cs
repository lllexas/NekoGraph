        │                                                              │
        │ 用途：等待游戏事件（如"单位死亡"、"建筑完成"）                │
        │                                                              │
        │ 典型连接：Mission-A → Trigger(UnitKilled) → Comparer         │
        │                                                              │
        │ 可编辑字段：Event                                            │
        │                                                              │
        │ 示例：等待"单位被击杀"事件 → Event = "UnitKilled"            │
