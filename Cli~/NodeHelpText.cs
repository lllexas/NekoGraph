        │ 示例引用：mission-s:main_quest_1                             │
        └─────────────────────────────────────────────────────────────┘

        ┌─────────────────────────────────────────────────────────────┐
        │ Mission-F (任务失败节点)                                      │
        ├─────────────────────────────────────────────────────────────┤
