        │                                                              │
        │ 用途：每个 Pack 必须有一个 Root 节点，作为剧情入口             │
        │                                                              │
        │ 典型连接：Root → Spine(第一关)                               │
        │                                                              │
        │ 示例引用：root_31622fd8 (使用 NodeID)                        │
        └─────────────────────────────────────────────────────────────┘

        ┌─────────────────────────────────────────────────────────────┐
        │ Spine (主干节点)                                             │
        ├─────────────────────────────────────────────────────────────┤
        │ 语义：流程的逻辑骨架，像"脊椎"一样支撑整个剧情结构            │
        │                                                              │
        │ 行为：收到信号 → 1) 激活关联的 Leaf-A 节点                    │
        │                2) 向下一个 Spine 传播信号                     │
        │                                                              │
        │ 字段：ProcessID（进程 ID，用于关联 Leaf 节点）                 │
