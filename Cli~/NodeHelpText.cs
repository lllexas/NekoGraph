        五、典型流程示例
        ═══════════════════════════════════════════════════════════════

        【简单任务流程】

        Root → Spine(main) → Leaf-A(main) → Mission-A(任务激活)
                                              ↓
                                    Trigger(等待"敌人被击杀")
                                              ↓
                                    Comparer(击杀数 >= 5?)
                                    ├─ Pass(端口 0) → Command(发放奖励)
                                    │                  ↓
                                    │              Mission-S(成功)
                                    │                  ↓
                                    │              Leaf-B(main)
                                    │                  ↓
                                    │              Spine(next)
                                    │
                                    └─ Fail(端口 1) → Mission-F(失败)

        【从 Comparer 的 Fail 端口接出分支】

        1. 查询包含 comparer 的桥接，获取其 NodeID：
           --query --bridge pack1 spine:main leaf-a:main

        2. 从 Fail 端口（端口 1）接出重试逻辑：
           --edit --create-bridge pack1 <comparerNodeId> mission-r:quest1
                                      trigger --from-port 1

        3. 设置 trigger 监听"任务重试"事件：
           --edit --field pack1 <comparerNodeId> mission-r:quest1 0
                              Event MissionCompleted

        ═══════════════════════════════════════════════════════════════
        六、常用命令速查
        ═══════════════════════════════════════════════════════════════

        查看节点详情：
          --show --node <packid> <node-ref>

        查看桥接路径：
          --query --bridge <packid> <from> <to> [--from-port N] [--to-port N]

        查看匿名节点字段：
          --query --fields <packid> <from> <to> <index>

        修改字段：
          --edit --field <packid> <from> <to> <index> <field> <value>

        创建桥接：
          --edit --create-bridge <packid> <from> <to> <kinds> [--from-port N]

        返回主帮助：
          --help

